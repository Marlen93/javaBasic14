package class4;

public class Task4 {

	public static void main(String[] args) {
		
		/*
		 * write a java program to check if number is even or odd
		 */
		
		int number=5;
		System.out.println(5%2);
		//if we take the modulus of a number by2 and answer is 0 it is Even 
		//otherwise it is an Odd number 
		
		if(number%2==0) {
			System.out.println("Number is Even");
		}else {
			System.out.println("Number is Odd");
		}
		
		
		

	}

}
