package com.syntax.class07;

public class ForLoopDemo {

	public static void main(String[] args) {

		for(int i=1; i<=5; i++) {
			System.out.println("Hello");
		}
		
		System.out.println("========");
		//print number from1-20
		
		for(int i=1; i<=20; i++) {
			System.out.println(i+" ");
		}
		System.out.println("====");
		//print numbers from 30-10

		for(int i=30; i>=10; i--){
			System.out.println(i+" ");
		}
		//what is the output
		
		for(int i=0; i<=10; i+=2) {
			System.out.println(i+" ");
		}
		
		
		
		
	}

}
